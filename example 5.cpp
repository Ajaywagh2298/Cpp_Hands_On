/* 5.	C++ Program to Find Size of int, float, double and char in Your System */
#include<iostream>
using namespace std;

int main(){
    int a;
    float b;
    char c;
    double d;
    bool e;

    cout<<"int\t"<<sizeof(a)<<endl;
    cout<<"float\t"<<sizeof(b)<<endl;
    cout<<"char\t"<<sizeof(c)<<endl;
    cout<<"double\t"<<sizeof(d)<<endl;
    cout<<"bool\t"<<sizeof(e)<<endl;

    return 0;

}
