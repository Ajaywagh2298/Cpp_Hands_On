/* 23.C++ Program to Multiply two Numbers */
#include<iostream>
using namespace std;

int main(){
    int first, second, result;

    cout<<"Enter the first number : ";
    cin>>first;

    cout<<"Enter the second number : ";
    cin>>second;

    result = first * second;

    cout<<"Multiplication of [ "<<first<<" * "<<second<<" ] = "<<result<<endl;

}
