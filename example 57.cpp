/* 57.C++ Program to Copy Strings */
#include <iostream>
using namespace std;
int main()
{
    string string1, string2;
    cout << "Enter string 1: ";
    cin>> string1;
    cout << " String 1  = "<< string1 <<endl;
    string2 = string1;

    cout << "Resultant String 2 = "<< string2 <<endl;
    return 0;
}
